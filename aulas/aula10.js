// operadores //
// - subtracao
// / divisao
// * multiplicacao
// ** potencia
// % resto

function soma(x, y){
    return x + y
}

function subtracao(x, y){
    return x - y
}

function divisao(x, y){
    return x / y
}

function multiplicacao(x, y){
    return x * y
}

function potencia(x, y){
    return x ** y
}

function resto(x, y){
    return x % y
}

console.log(soma(20,10));
console.log(subtracao(20,10));
console.log(divisao(20,10));
console.log(multiplicacao(20,10));
console.log(potencia(20,2));
console.log(resto(20,10));