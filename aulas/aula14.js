function teste(key){
    switch (key) {
        case 'Gabriel':
        case 'Leonardo':
            console.log('Gabriel aqui');
            break;
        case 'Jose':
            console.log('Jose aqui');
            break;
        case 'Pedro':
            console.log('Pedro aqui');
            break;
        default:
            console.log('Qualquer outro');
            break;
    }
}

teste('Pedro');

